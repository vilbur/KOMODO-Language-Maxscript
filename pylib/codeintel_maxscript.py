#---- CILE Driver class

# Dev Notes:
# A CILE (Code Intelligence Language Engine) is the code that scans
# Maxscript content and returns a description of the code in that file.
# See "cile_maxscript.py" for more details.
#
# The CILE Driver is a class that calls this CILE. If Maxscript is
# multi-lang (i.e. can contain sections of different language content,
# e.g. HTML can contain markup, JavaScript and CSS), then you will need
# to also implement "scan_multilang()".
class MaxscriptCILEDriver(UDLCILEDriver):
    lang = lang

    def scan_purelang(self, buf):
        import cile_maxscript
        return cile_maxscript.scan_buf(buf)




